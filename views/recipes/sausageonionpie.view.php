              <h2>method</h2>
              <p>
                I use a 20cm x 5cm deep round glass Pirex dish to make this pie
                and use parchment paper and uncooked rice to blind bake the base
                of the pie - we don't like soggy bottoms here!
              </p>
              <p>
                <b>Note:</b> If you want to use shop bought, ready to roll
                pastry, make sure it is <b>All butter pastry</b> if it doesn't
                say <b>all butter</b> on the packet, don't buy it! Skip to No.8
                on the list if you use shop bought
              </p>
              <p>
                The pastry I make here is mouth wateringly crumbly, don't worry
                if it breaks when laying the base of the pastry into the dish,
                just patch it up by pressing bits of pastry into the broken
                gaps. No one will know when it's finished!
              </p>
              <p>
                <b>Tip:</b> Make sure your hands are cold before you start
                rubbing the butter into the flour
              </p>
              <p>
                <b>Tip:</b> Save the rice you used to blind bake the base, you
                can use this again and again
              </p>
              <h2>make the pastry</h2>
              <!-- Method List Goes Below Here!!! -->

              <ol>
                <li>Tip the flour into a large mixing bowl</li>
                <li>Add the cold cubes of butter</li>
                <li>Rub the butter into the flour using finger tips</li>
                <li>When you have a fine crumb, add the water</li>
                <li>Mix the water into the crumb to form the pastry</li>
                <li>Knead the dough lightly to form a ball</li>
                <li>Wrap in plastic food film and refrigerate for 1/2 hr</li>
                <li>Dust a wooden board or work surface with a little flour</li>
                <li>
                  Take <b>2/3</b> of the pastry to form a 4mm thick circle
                </li>
                <li>Grease the pie dish all over with butter</li>
                <li>Gently fold the pastry over a rolling pin</li>
                <li>Lift the pastry onto the pie dish</li>
                <li>Gently press the pastry into edges of the dish</li>
                <li>Prick the base of the pastry all over with a fork</li>
                <li>Put the parchment paper over the dish</li>
                <li>Press the paper into the dish & fill with uncooked rice</li>
                <li>Bake at 180c for 10 mins, center of the oven</li>
                <li>Remove the parchment paper & rice</li>
                <li>Put back in the oven for 10 mins more</li>
                <li>Take the pastry out of the oven & allow to cool</li>
              </ol>

              <div class="spacer"></div>
              <h2>Make the filling</h2>

              <ol>
                <li>Put the onion into a microwave proof dish</li>
                <li>Sprinkle with the oil, salt & pepper</li>
                <li>Mix well & microwave for 1 min</li>
                <li>Half the patty meat & press into the pastry dish</li>
                <li>Cover the meat with 1/2 of the onion slices</li>
                <li>cover the onion with the rest of the meat</li>
                <li>Cover the meat with the onion slices</li>
              </ol>

              <div class="spacer"></div>
              <h2>finish the pie</h2>

              <ol>
                <li>
                  Roll out the rest of the pastry into a 4 mm thick circle
                </li>

                <li>Top the dish with the pastry</li>
                <li>Press the pastry into the edges of the dish</li>
                <li>Don't trim the pastry yet!</li>
                <li>Bake in the oven at 180c for 1 hr</li>
                <li>Take the pie out of the oven when golden brown</li>
                <li>Trim the edges of the pastry with a sharp knife</li>
                <li>Allow to cool enough to handle</li>
              </ol>

