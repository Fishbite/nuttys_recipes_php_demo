            <h2>method</h2>
            <p>
              I use a food mixer for this recipe, but you can do it by hand,
              just make sure your butter is really soft. So, pop the oven on
              at 180c and grease an oven proof dish. By the time the oven's
              hot you'll be ready to rock!
            </p>
            <p>
              By the way, you don't need to wash the mixer / mixing bowl out
              after making the topping, just scrape it clean with a spatula
              and you are ready to go with the cake mix ingredients
            </p>
            <!-- Method List Goes Below Here!!! -->

            <ol>
              <li>Put the topping ingredients in a food mixer</li>
              <li>Set it to a medium speed</li>
              <li>Let the mixer run until butter and sugar are combined</li>
              <li>
                Grease an oven proof dish - rub butter around the sides & base
                of the dish
              </li>
              <li>Spread topping into the base of the dish</li>
              <li>Place the pineapple slices on the topping</li>
            </ol>

            <div class="spacer"></div>
            <div class="card_image">
              <figcaption>layer the pineapple slices</figcaption>
              <img src="/images/pineapplepud/650x366/topping-and-pineapple.jpg" alt="topping and pineapple" />
            </div>
            <div class="spacer"></div>

            <ol start="7">
              <li>Put the first 5 cake ingredients into the food mixer</li>
              <li>Turn the mixer on to medium speed</li>
              <li>Allow the ingredients to combine</li>
              <li>Tip in the flour & baking powder</li>
              <li>Allow to combine again</li>
            </ol>

            <div class="spacer"></div>
            <p>
              <b>NB:</b> If mixing by hand, sift the flour & baking powder
              into a bowl through a sieve
            </p>

            <ol start="12">
              <li>Pour the cake mixture onto the pineapple</li>
            </ol>

            <div class="spacer"></div>
            <div class="card_image">
              <figcaption>top with the cake mixture</figcaption>
              <img src="/images/pineapplepud/650x366/dish-with-cakemix.jpg" alt="dish with cake mix" />
            </div>
            <div class="spacer"></div>

            <ol start="13">
              <li>Bake in pre-heated oven @ 180c for 45mins</li>
              <li>Use a wooden skewer to check for doneness*</li>
            </ol>

            <div class="spacer"></div>
            <p>
              *Prick the center of the cake with the skewer, it should come
              out dry (except the very tip which will have caramel on it) when
              the cake is cooked
            </p>

            <ol start="15">
              <li>Allow to cool for a while so you can handle it</li>
            </ol>

            <div class="spacer"></div>
            <div class="card_image">
              <figcaption>allow to cool</figcaption>
