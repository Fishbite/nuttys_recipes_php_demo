            <h2>serves 4</h2>
            <h2>Meat</h2>

            <ul>
              <li>375g diced pork shoulder*</li>
              <li>4 tsp five spice powder</li>
              <li>1 tsp salt</li>
              <li>1 tsp ground black pepper</li>
            </ul>
            <div class="spacer"></div>
            <p>
              *Marinate overnight, when ready to cook add the ingredients
              below to your marinate pot and shake.
            </p>
            <ul>
              <li>Juice of one lemon & zest</li>
              <li>5cm piece of fresh ginger diced</li>
              <li>2 cloves garlic finely diced</li>
              <li>1 Tbsp oil</li>
            </ul>
            <div class="spacer"></div>

            <h2>for the Sauce</h2>

            <ul>
              <li>1 Tbsp butter</li>
              <li>1/2 white onion very finely diced</li>
              <li>5cm ginger finely diced</li>
              <li>3 cloves garlic finely diced</li>
              <li>2 Tbsp brown sugar</li>
              <li>Juice from pineapple tin</li>
              <li>Water + pineapple juice = 1/2 pint (284 ml)</li>
              <li>1 Tbsp cornflour</li>
              <li>1 Tbsp Sriracha hot chilli sauce ( Optional )</li>
              <li>4 Tbsp Shaohsing rice wine vinegar*</li>
              <li>1 OXO&trade; vegetable stock cube crumbled</li>
            </ul>
            <div class="spacer"></div>
            <p>
              *Substitute Shaohsing rice wine vinegar with dry white sherry or
              a white wine vinegar if you need to.
            </p>

            <h2>Vegetables</h2>

            <ul>
              <li>4 small tomatoes quartered</li>
              <li>8 thick slices of cucumber</li>
              <li>425g tin pineapples quartered</li>
              <li>1/2 Bunch spring onions</li>
              <li>1 green chilli sliced</li>
              <li>Pinch of salt and pepper</li>
            </ul>

