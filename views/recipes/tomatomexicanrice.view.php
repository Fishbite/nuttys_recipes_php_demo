                  />
                </div>
                <div class="spacer"></div>

              <ol start="4">
                <li>Wait until chilli flakes brown & cumin odour fills the kitchen</li>
                <li>Add onions, cook until soft <b>not</b> brown</li>
                <li>Add garlic salt and all the spices</li>
                <li>stir-fry until all is combined, be careful spices burn quickly!</li>
              </ol>
                

                <div class="spacer"></div>
                <div class="card_image">
                  <figcaption>
